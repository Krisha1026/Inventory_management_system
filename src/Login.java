    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        
        // 1. Get user input
    String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();
    
    // 2. Validate role selection
    if (!radioCashier.isSelected() && !radioManager.isSelected()) {
        JOptionPane.showMessageDialog(this, "Please select a role!");
        return;
    }
    
    // 3. Check credentials
    if (radioCashier.isSelected() && username.equals("cashier1") && password.equals("password")) {
        JOptionPane.showMessageDialog(this, "Login successful as Cashier");
        this.setVisible(false); // Hide login window
        new CashierMenu().setVisible(true); // Open cashier menu
        this.dispose(); // Release resources
    } 
    else if (radioManager.isSelected() && username.equals("admin") && password.equals("admin123")) {
        JOptionPane.showMessageDialog(this, "Login successful as Manager");
        this.setVisible(false);
        new ManagerMenu().setVisible(true);
        this.dispose();
    } 
    else {
        JOptionPane.showMessageDialog(this, "Invalid credentials!");
    }

    }//GEN-LAST:event_BtnLoginActionPerformed

