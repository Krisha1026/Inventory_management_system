    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {                                         
        
         // Get user input
    String username = txtUsername.getText();
    String password = new String(txtPassword.getPassword());

    // Check which role is selected
    String role = "";
    if (radioCashier.isSelected()) {
        role = "Cashier";
    } else if (radioManager.isSelected()) {
        role = "Manager";
    }

     // Validate login (dummy check)
    if (role.equals("Cashier") && username.equals("cashier1") && password.equals("password")) {
        JOptionPane.showMessageDialog(this, "Login successful as Cashier");
        // Open Cashier menu
        CashierMenu cashierMenu = new CashierMenu();
        cashierMenu.setVisible(true);
        this.dispose(); // Close login page
    } else if (role.equals("Manager") && username.equals("admin") && password.equals("admin123")) {
        JOptionPane.showMessageDialog(this, "Login successful as Manager");
        // Open Manager menu
        ManagerMenu managerMenu = new ManagerMenu();
        managerMenu.setVisible(true);
        this.dispose(); // Close login page
    } else {
        JOptionPane.showMessageDialog(this, "Invalid username, password or role!");
    }
        
    }
